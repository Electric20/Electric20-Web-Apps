<h1>Participating in Electric20</h1>
<p>Dear visitor,</p>
<p>
Thank you for expressing interest in the current electricity monitoring and feedback trial being run by <a href="http://www.horizon.ac.uk/" target="new">Horizon Digital Economy Research Institute</a> and the <a href="http://www.mrl.nott.ac.uk/" target="new">Mixed 
Reality 
Laboratory</a> at the University of Nottingham. 
We 
are always happy 
to hear from households who would 
like to get involved, but there are some practical requirements that we need to check with each home before we can add you to our cohort of participants. For your convenience, we have outlined these requirements below.
</p>
<div class="innercontainer" style="padding: 20px; color: black;">

<div style="overflow: hidden;">
<img style="padding-left: 20px; float: right;" src="clamp.png" />
<h2>Is your electricity meter accessible?</h2>
<p style="font-weight: bold; color: green;">We require physical access to wires running from your electricity meter once before you can participate</p>
<p>
The first requirement for participation in our trial is that we can retrieve meter readings from your electricity meter. Unlike your electricity company, we do this <span style="font-weight: bold;">remotely</span> rather than by sending a technician to come and 
read 
your 
meter.
</p>
<p>Before we can do this, we 
need to be able to gain physical access to the wires that lead from your 
electricity meter 
to the fuse-box in your home. In most houses, the electricity meter is readily accessible: you may access it yourself in order to give your electricity company accurate readings.
If we can access a suitable wire, we place a simple clamp around a section of the wire which measures the electricity flowing through the wire.</p>
<p>The clamp then sends readings wirelessly to a small display which we place in your home. 
Once the clamp is in place and is sending readings to the display, we no longer need to physically access your meter to take meter readings.
</p>
<p style="font-style: italic;">The photo to the right shows a clamp and wireless transmitter suitably fitted to a wire running from an electricity meter.</p>
<p>If we cannot access the wires running from your electricity meter to your home, or if a suitable section of wire is too far from your home for the clamp's wireless signal to reach the display (this may be the case in 
apartment buildings where a communal meter room may be far from your apartment), we will not be able to include you in our study.
</p>
</div>

<div style="overflow: hidden; padding-top: 30px;">
<img style="padding-right: 20px; float: left;" src="ethernet.png" />
<h2>Can your home access the internet?</h2>
<p style="font-weight: bold; color: green;">We require that you have a home broadband connection, and a broadband router with a spare Ethernet socket (or other means of connecting a device to the internet via Ethernet cable) before you can participate</p>
<p>
Once the electricity monitoring equipment is successfully installed, meter readings are sent regularly (up to 10 times a minute) to the Electric20 data store via the internet. The electricity display cannot do this alone, so we require that particpants allow us to 
install a small low-power computer (known as a "SheevaPlug") in your home alongside the display. We connect the SheevaPlug to your home broadband connection to pass meter readings from the display to the Electric20 data store.
</p>
<p>The SheevaPlug connects to your home computer network like any other personal computer or laptop, but it does not have wireless capabilities and so requires that it be connected to your home broadband router (e.g. a BT Home Hub) via a cable. In the majority of 
cases your router 
will have several Ethernet sockets for this purpose (they may be labelled "Ethernet" or "LAN"). If you are unsure whether you meet this requirement, please <a class="dark" href="mailto:info@electric20.com">email the Electric20 team</a> and let us know who supplies 
your 
broadband 
connection and we will help 
you check.</p>
<p style="font-style: italic;">The photo to the left shows the back of a home broadband router; in this case there are four Ethernet sockets (coloured yellow) that could be used by the SheevaPlug.</p>
</div>

<div style="overflow: hidden; padding-top: 30px;">
<h2>Can you commit your time to discuss your experience with us?</h2>
<p style="font-weight: bold; color: green;">We require 2-3 hours of your time, split between 2-3 home visits during the course of the 10 weeks of the study, to discuss aspects of the way your household uses electricity and technology</p>
<p>
As a result of the Electric20 trial, we hope to find out how the public incorporate electricity monitoring and feedback technologies into their home lives. We expect that introducing these technologies will change some of your routines, or your views on the 
technologies, and we measure these changes by speaking with you before, during and after the trial.
Based on previous experience, these discussions may be as short 
as 30 minutes each, or as long as a couple of hours (depending on how much you have to say!). For the final discussion we would highly recommend that all members of the household are present, so that we have the best picture of the household's experience during the 
trial.
</p>
<p>
While we try to make these discussions fairly informal (and have them in the comfort of your own home) we do ask that every discussion is recorded so that we do not miss any facts or opinions you might express. If the household has a number of occupants, we may ask 
permission to record video of the discussion rather than audio alone, as this helps us to identify who says what if we replay the discussion later on.
</p>
